clear; clc; close all;

%% =========================================================
%% KPZ : W(t) vs t FOR DIFFERENT LAMBDA VALUES
%%
%% dh/dt =
%%
%% nu * nabla^2 h
%% + (lambda/2)|grad h|^2
%% - kappa*h
%% + eta
%%
%% Periodic 2D flat domain
%%
%% =========================================================

%% =========================================================
%% FIXED PHYSICAL PARAMETERS
%% =========================================================

nu       = 0.25;
kappa    = 0.0;
noiseAmp = 1.0;

v0 = 0.0;

%% =========================================================
%% LAMBDA VALUES
%% =========================================================

lambda_arr = [0 0.5 1 1.5 2.0];

nCases = length(lambda_arr);

%% =========================================================
%% NUMERICS
%% =========================================================

Nx = 256;
Ny = 256;

Lx = 100;
Ly = 100;

dx = Lx/Nx;
dy = Ly/Ny;

Nt = 100000;

dt = 1e-3;

%% =========================================================
%% GRID
%% =========================================================

x = linspace(0,Lx,Nx);
y = linspace(0,Ly,Ny);

[X,Y] = meshgrid(x,y);

time = (0:Nt-1)*dt;

%% =========================================================
%% STORAGE
%% =========================================================

W_all = zeros(Nt,nCases);

%% =========================================================
%% COLORS
%% =========================================================

colors = [
    0.3 0.9 0.9
    0.2 0.4 0.9
    0.9 0.3 0.2
    0.2 0.7 0.3
    0.6 0.2 0.7
];

%% =========================================================
%% LOOP OVER LAMBDA
%% =========================================================

for icase = 1:nCases

    %% -----------------------------------------------------
    %% current lambda
    %% -----------------------------------------------------

    lambda = lambda_arr(icase);

    disp(['Running lambda = ',num2str(lambda)])

    %% -----------------------------------------------------
    %% initial condition
    %% -----------------------------------------------------

    h = 0.01*randn(Ny,Nx);

    %% -----------------------------------------------------
    %% storage
    %% -----------------------------------------------------

    W = zeros(Nt,1);

    %% =====================================================
    %% TIME LOOP
    %% =====================================================

    for it = 1:Nt

        %% -------------------------------------------------
        %% periodic neighbors
        %% -------------------------------------------------

        hL = circshift(h,[0 +1]);
        hR = circshift(h,[0 -1]);

        hU = circshift(h,[+1 0]);
        hD = circshift(h,[-1 0]);

        %% -------------------------------------------------
        %% gradients
        %% -------------------------------------------------

        dhdx = (hR - hL)/(2*dx);

        dhdy = (hD - hU)/(2*dy);

        %% -------------------------------------------------
        %% Laplacian
        %% -------------------------------------------------

        d2hdx2 = (hR - 2*h + hL)/(dx^2);

        d2hdy2 = (hD - 2*h + hU)/(dy^2);

        LapH = d2hdx2 + d2hdy2;

        %% -------------------------------------------------
        %% KPZ nonlinear term
        %% -------------------------------------------------

        grad2 = dhdx.^2 + dhdy.^2;

        %% -------------------------------------------------
        %% stochastic noise
        %% -------------------------------------------------

        eta = (noiseAmp/sqrt(dt))*randn(Ny,Nx);

        %% -------------------------------------------------
        %% Euler update
        %% -------------------------------------------------

        h_new = h + dt*(...
            v0 ...
            + nu*LapH ...
            + 0.5*lambda*grad2 ...
            - kappa*h ...
            + eta);

        %% -------------------------------------------------
        %% optional clipping
        %% -------------------------------------------------

        % h_new(abs(h_new)>50) = 50;

        h = h_new;

        %% -------------------------------------------------
        %% roughness
        %% -------------------------------------------------

        hbar = mean(h(:));

        W(it) = sqrt(mean((h(:)-hbar).^2));

    end

    %% =====================================================
    %% smooth roughness
    %% =====================================================

    W_smooth = sgolayfilt(W,3,31);

    %% =====================================================
    %% store
    %% =====================================================

    W_all(:,icase) = W_smooth;

end

%% =========================================================
%% PLOT : W(t) vs t
%% =========================================================

figure('Color','w',...
    'Position',[100 100 900 800])

for icase = 1:nCases

    loglog(time,...
        W_all(:,icase),...
        '-','LineWidth',4,...
        'Color',colors(icase,:),...
        'DisplayName',...
        ['\lambda = ',num2str(lambda_arr(icase))]);

    hold on

end

%% ---------------------------------------------------------
%% reference scalings
%% ---------------------------------------------------------

loglog(time,...
    1*time.^(1/2),...
    '-.k',...
    'LineWidth',2,...
    'DisplayName','t^{1/2}')

hold on

loglog(time,...
    0.55*time.^(1/3),...
    '--k',...
    'LineWidth',2,...
    'DisplayName','t^{1/3}')

hold on

loglog(time,...
    0.8*time.^(1/4),...
    ':k',...
    'LineWidth',2,...
    'DisplayName','t^{1/4}')

%% =========================================================
%% aesthetics
%% =========================================================

xlabel('t',...
    'FontSize',40)

ylabel('W(t)',...
    'FontSize',40)

legend('Location','northwest',...
    'FontSize',22,...
    'Box','off')

axis square

set(gca,...
    'FontSize',28,...
    'LineWidth',3,...
    'TickLength',[0.02 0.02],...
    'TickDir','out')

box on

set(gcf,'Renderer','Painter')

